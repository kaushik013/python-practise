

# ⁡⁢⁣⁣escape character⁡

# ⁡⁣⁣⁢1. \"⁡
# print("He said, "Hello"")   # ❌ Error (quotes inside quotes)

print("hello said, \"hello\"") # hello said, "hello"


# ⁡⁣⁣⁢2. \'⁡
print("it\'s kaushik" ) # it's kaushik


# ⁡⁣⁣⁢3. \\ ⁡
print('\\\\kaushik')  # \\kaushik


# ⁡⁣⁣⁢4. \n⁡
print('hello \nthis is python') # new line


# ⁡⁣⁣⁢5. \t (tab)⁡
print('kaushik \t chariya') # kaushik          chariya\

# ⁡⁣⁣⁢6. \b (remove last char --> Backspace)⁡
print("kaushik\b")


# ⁡⁣⁣⁢7. \r (replace before)⁡
print('code \rpython') #


# ⁡⁣⁣⁢8. \f ()⁡
print('hello \fworld') # hello 
                              #  world
                              