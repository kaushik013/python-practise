# 4 ⁡⁣⁣⁢bool(boolean)⁡

young = True
print(young) # True
print(type(young)) # <class 'bool'>


# 5 ⁡⁣⁣⁢list⁡ 
# store multiple value in one variable
# list are mutable (changable) and written in square brackets []

fruits = ['Apple', 'Banana','Graps']
print(fruits) # ['Apple', 'Banana', 'Graps']
print(type(fruits)) # <class 'list'>


# 6 ⁡⁣⁣⁢touple⁡
# similar to list but cant change (immutable)
# writen in parentheses ()

number = (11,22,33,44,55)
print(number) # (11, 22, 33, 44, 55)
print(type(number)) # <class 'tuple'>


