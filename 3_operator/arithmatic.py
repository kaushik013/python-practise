p = 100
print(o // p) # 2



# ⁡⁢⁣⁣6. % (modulus)⁡

# --> modulus operator is giving the reminder after dividing one number by another 
# it doesn't give the quotient(the result of division) only the ⁡⁢⁣⁣remainder⁡ art


g = 200
h = 100
print(g % h) # 2.0


